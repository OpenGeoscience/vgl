//////////////////////////////////////////////////////////////////////////////
/**
 * @module vgl
 */

/*global vgl*/
//////////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////////////
/**
 * Create a new instance of class legend
 *
 * @class vgl.legend
 * @description legend class is intended to create legend for 2D/3D scene.
 *
 * @returns {vgl.legend}
 */
////////////////////////////////////////////////////////////////////////////
vgl.legend = function () {
  'use strict';

  var m_lookupTable = null;

  if (!(this instanceof vgl.legend)) {
    return new vgl.legend();
  }
  vgl.actor.call(this);

  ////////////////////////////////////////////////////////////////////////////
  /**
   *
   * @returns {*}
   */
  ////////////////////////////////////////////////////////////////////////////
  this.lookupTable = function () {
    return m_lookupTable;
  };

  ////////////////////////////////////////////////////////////////////////////
  /**
   *
   * @param lookupTable
   */
  ////////////////////////////////////////////////////////////////////////////
  this.setLookupTable = function (lookupTable) {
    m_lookupTable = lookupTable;
    this.modified();
  };

  return this;
};
